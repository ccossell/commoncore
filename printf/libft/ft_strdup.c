#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*str;
	int		i;

	i = 0;
	size = ft_strlen(s1);
	str = (char *)malloc(size + 1);
	if (str == NULL)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
